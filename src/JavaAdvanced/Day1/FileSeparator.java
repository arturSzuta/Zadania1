package JavaAdvanced.Day1;

import java.io.File;

public class FileSeparator {
    public static void main(String[] args) {
        System.out.println("File separator: " + File.separator);
        System.out.println("File separator char: " + File.separatorChar);
        System.out.println("File path separator : " + File.pathSeparator);
        System.out.println("File path separator char: " + File.pathSeparatorChar);
    }
}
